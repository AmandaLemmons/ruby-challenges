weather = 'snowing'

case weather
when 'sunny'
    puts "Better take your sunglasses!"
when 'rainy'
    puts "Don't forget to grab that umbrella."
when 'cloudy'
    puts "Grab that scarf on your way out."
when 'hot'
    puts "Wear shorts and drink lots of water!"
else
    puts "Stay in your PJs and put on some Netflix."
end